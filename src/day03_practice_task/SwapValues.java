package day03_practice_task;

public class SwapValues {

    public static void main(String[] args) {

        // By using Temporary Variables

        int num1 = 5;
        int num2 = 10;
        int temp;

        temp = num1;
        num1 = num2;
        num2 = temp;

        System.out.println("num1 = " + num1);
        System.out.println("num2 = " + num2);

      /*
      Write a code to swap the values of 2 int variables using a temporary variable.
      num1 = 5;
      num2 = 10;
       */

    }
}
