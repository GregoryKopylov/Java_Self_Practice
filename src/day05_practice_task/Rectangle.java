package day05_practice_task;

public class Rectangle {

    public static void main(String[] args) {

        int givenNumber = 8;


        for (int i = 0; i <= givenNumber; i++) {


            for (int j = 1; j <= givenNumber; j++) {
                System.out.print("* ");

            }

            System.out.println();
        }
    }
}